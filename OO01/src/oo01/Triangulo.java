public class Triangulo {
    int cantLados;
    float base;
    float altura;
    char letra;
    int dimensiones;
    
    Triangulo (char letra, int cantLados, int base, int altura, int dimensiones) {
    this.letra = letra;
    this.cantLados = cantLados;
    this.base = base;
    this.altura = altura;
    this.dimensiones = dimensiones;
    }     
}

    
